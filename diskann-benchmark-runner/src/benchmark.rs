pub(crate) mod internal {
    use super::*;

    use crate::{input::internal::Any, Features};

    use anyhow::Context;
    use thiserror::Error;

    #[derive(Debug)]
    pub(crate) enum AnnotatedMatch<'a> {
        /// User matching indicated success.
        User(Score),
        /// We missed because the input type was incorrect.
        WrongTag,
        /// We missed because the input type matches, but the benchmark is feature-gated.
        Gated(&'a Features),
    }

    impl AnnotatedMatch<'_> {
        pub(crate) fn try_into_score(self) -> Option<Score> {
            if let Self::User(score) = self {
                Some(score)
            } else {
                None
            }
        }
    }

    /// Object-safe trait for type-erased benchmarks stored in the registry.
    pub(crate) trait Benchmark {
        fn try_match(&self, input: &Any, context: &MatchContext) -> AnnotatedMatch<'_>;

        fn description(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result;

        fn run(
            &self,
            input: &Any,
            checkpoint: Checkpoint<'_>,
            output: &mut dyn Output,
        ) -> anyhow::Result<serde_json::Value>;

        fn visibility(&self) -> Visibility<'_>;

        /// If supported, return an object capable of running regression checks on this benchmark.
        fn as_regression(&self) -> Option<&dyn Regression>;

        fn as_string(&self) -> String {
            Description(self).to_string()
        }
    }

    struct Description<'a, T: ?Sized>(&'a T);

    impl<T> std::fmt::Display for Description<'_, T>
    where
        T: Benchmark + ?Sized,
    {
        fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
            self.0.description(f)
        }
    }

    pub(crate) struct Checked {
        pub(crate) json: serde_json::Value,
        pub(crate) display: Box<dyn std::fmt::Display>,
    }

    impl Checked {
        /// Serialize `value` to `serde_json::Value` and box it for future display.
        fn new<T>(value: T) -> Result<Self, serde_json::Error>
        where
            T: Serialize + std::fmt::Display + 'static,
        {
            Ok(Self {
                json: serde_json::to_value(&value)?,
                display: Box::new(value),
            })
        }
    }

    pub(crate) type CheckedPassFail = PassFail<Checked, Checked>;

    pub(crate) trait Regression {
        fn tolerance(&self) -> &dyn crate::input::internal::DynInput;
        fn input_tag(&self) -> &'static str;
        fn check(
            &self,
            tolerances: &Any,
            input: &Any,
            before: &serde_json::Value,
            after: &serde_json::Value,
        ) -> anyhow::Result<CheckedPassFail>;
    }

    impl std::fmt::Debug for dyn Regression + '_ {
        fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
            f.debug_struct("dyn Regression")
                .field("tolerance", &self.tolerance().tag())
                .field("input_tag", &self.input_tag())
                .finish()
        }
    }

    pub(crate) trait AsRegression<T> {
        fn as_regression(benchmark: &T) -> Option<&dyn Regression>;
    }

    #[derive(Debug, Clone, Copy)]
    pub(crate) struct NoRegression;

    impl<T> AsRegression<T> for NoRegression {
        fn as_regression(_benchmark: &T) -> Option<&dyn Regression> {
            None
        }
    }

    #[derive(Debug, Clone, Copy)]
    pub(crate) struct WithRegression;

    impl<T> AsRegression<T> for WithRegression
    where
        T: super::Regression,
    {
        fn as_regression(benchmark: &T) -> Option<&dyn Regression> {
            Some(benchmark)
        }
    }

    impl<T> Regression for T
    where
        T: super::Regression,
    {
        fn tolerance(&self) -> &dyn crate::input::internal::DynInput {
            &crate::input::internal::Wrapper::<T::Tolerances>::INSTANCE
        }

        fn input_tag(&self) -> &'static str {
            T::Input::tag()
        }

        fn check(
            &self,
            tolerance: &Any,
            input: &Any,
            before: &serde_json::Value,
            after: &serde_json::Value,
        ) -> anyhow::Result<CheckedPassFail> {
            let tolerance = tolerance
                .downcast_ref::<T::Tolerances>()
                .ok_or_else(|| BadDownCast::new(T::Tolerances::tag(), tolerance.tag()))
                .context("failed to obtain tolerance")?;

            let input = input
                .downcast_ref::<T::Input>()
                .ok_or_else(|| BadDownCast::new(T::Input::tag(), input.tag()))
                .context("failed to obtain input")?;

            let before = T::Output::deserialize(before)
                .map_err(|err| DeserializationError::new(Kind::Before, err))?;

            let after = T::Output::deserialize(after)
                .map_err(|err| DeserializationError::new(Kind::After, err))?;

            let passfail = match self.check(tolerance, input, &before, &after)? {
                PassFail::Pass(pass) => PassFail::Pass(Checked::new(pass)?),
                PassFail::Fail(fail) => PassFail::Fail(Checked::new(fail)?),
            };

            Ok(passfail)
        }
    }

    #[derive(Debug, Clone, Copy)]
    pub(crate) struct Wrapper<T, R = NoRegression> {
        benchmark: T,
        _regression: R,
    }

    impl<T, R> Wrapper<T, R> {
        pub(crate) const fn new(benchmark: T, regression: R) -> Self {
            Self {
                benchmark,
                _regression: regression,
            }
        }
    }

    impl<T, R> Benchmark for Wrapper<T, R>
    where
        T: super::Benchmark,
        R: AsRegression<T>,
    {
        fn try_match(&self, input: &Any, context: &MatchContext) -> AnnotatedMatch<'_> {
            if let Some(cast) = input.downcast_ref::<T::Input>() {
                AnnotatedMatch::User(self.benchmark.try_match(cast, context))
            } else {
                AnnotatedMatch::WrongTag
            }
        }

        fn description(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
            writeln!(f, "tag \"{}\"", <T::Input as Input>::tag())?;
            self.benchmark.description(f)
        }

        fn run(
            &self,
            input: &Any,
            checkpoint: Checkpoint<'_>,
            output: &mut dyn Output,
        ) -> anyhow::Result<serde_json::Value> {
            match input.downcast_ref::<T::Input>() {
                Some(input) => {
                    let result = self.benchmark.run(input, checkpoint, output)?;
                    Ok(serde_json::to_value(result)?)
                }
                None => Err(BadDownCast::new(T::Input::tag(), input.tag()).into()),
            }
        }

        fn visibility(&self) -> Visibility<'_> {
            Visibility::Available
        }

        // Extensions
        fn as_regression(&self) -> Option<&dyn Regression> {
            R::as_regression(&self.benchmark)
        }
    }

    //--------//
    // Errors //
    //--------//

    #[derive(Debug, Clone, Copy, Error)]
    #[error(
        "INTERNAL ERROR: bad downcast - expected \"{}\" but got \"{}\"",
        self.expected,
        self.got
    )]
    struct BadDownCast {
        expected: &'static str,
        got: &'static str,
    }

    impl BadDownCast {
        fn new(expected: &'static str, got: &'static str) -> Self {
            Self { expected, got }
        }
    }

    #[derive(Debug, Error)]
    #[error(
        "the \"{}\" results do not match the output schema expected by this benchmark",
        self.kind
    )]
    struct DeserializationError {
        kind: Kind,
        source: serde_json::Error,
    }

    impl DeserializationError {
        fn new(kind: Kind, source: serde_json::Error) -> Self {
            Self { kind, source }
        }
    }

    #[derive(Debug, Clone, Copy)]
    enum Kind {
        Before,
        After,
    }

    impl std::fmt::Display for Kind {
        fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
            let as_str = match self {
                Self::Before => "before",
                Self::After => "after",
            };

            write!(f, "{}", as_str)
        }
    }

    // Gated Benchmarks
    //
    // We support two flavors:
    //
    // 1. Benchmarks that have inputs that are valid and registered.
    // 2. Benchmarks that have inputs that are *also* feature gated.

    /// A benchmark that is gated behind `features`, but whose input *is* compiled into the
    /// application. These benchamrks allow their inputs to be inspected and parsed, but
    /// fail matching.
    ///
    /// This custom type allows us to return more precise error messages when benchmark
    /// matching fails.
    pub(crate) struct PartiallyGated<I> {
        features: Features,
        description: String,
        _input: std::marker::PhantomData<I>,
    }

    impl<I> PartiallyGated<I> {
        pub(crate) fn new(features: Features, description: String) -> Self {
            Self {
                features,
                description,
                _input: std::marker::PhantomData,
            }
        }
    }

    impl<I> Benchmark for PartiallyGated<I>
    where
        I: Input,
    {
        fn try_match(&self, input: &Any, _context: &MatchContext) -> AnnotatedMatch<'_> {
            if input.is::<I>() {
                AnnotatedMatch::Gated(&self.features)
            } else {
                AnnotatedMatch::WrongTag
            }
        }

        fn description(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
            writeln!(f, "tag \"{}\"", I::tag())?;
            f.write_str(&self.description)
        }

        fn run(
            &self,
            _input: &Any,
            _checkpoint: Checkpoint<'_>,
            _output: &mut dyn Output,
        ) -> anyhow::Result<serde_json::Value> {
            anyhow::bail!("tried to run a gated benchmark - this should be unreachable")
        }

        fn visibility(&self) -> Visibility<'_> {
            Visibility::Gated {
                features: &self.features,
            }
        }

        fn as_regression(&self) -> Option<&dyn Regression> {
            None
        }
    }

    /// A benchmark that is gated behind `features` along with its input.
    ///
    /// These benchmarks are completely unreachable as the front-end has no way to parse
    /// inputs associated with `tag`.
    ///
    /// However, they are included to provide a bread-crumb for users that additional
    /// benchmarks *do* exist behind the associated features.
    pub(crate) struct FullyGated {
        tag: &'static str,
        features: Features,
        description: String,
    }

    impl FullyGated {
        pub(crate) fn new(tag: &'static str, features: Features, description: String) -> Self {
            Self {
                tag,
                features,
                description,
            }
        }
    }

    impl Benchmark for FullyGated {
        fn try_match(&self, _input: &Any, _context: &MatchContext) -> AnnotatedMatch<'_> {
            AnnotatedMatch::WrongTag
        }

        fn description(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
            writeln!(f, "tag \"{}\"", self.tag)?;
            f.write_str(&self.description)
        }

        fn run(
            &self,
            _input: &Any,
            _checkpoint: Checkpoint<'_>,
            _output: &mut dyn Output,
        ) -> anyhow::Result<serde_json::Value> {
            anyhow::bail!("tried to run a gated benchmark - this should be unreachable")
        }

        fn visibility(&self) -> Visibility<'_> {
            Visibility::Gated {
                features: &self.features,
            }
        }

        fn as_regression(&self) -> Option<&dyn Regression> {
            None
        }
    }
}

